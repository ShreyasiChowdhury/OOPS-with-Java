package calculation;
public class Operations
{
    public static int sum(int a,int b)
    {
        return a+b;
    }
    public static int multiply(int a,int b)
    {
        return a*b;
    }
    public static double sum(double a,double b)
    {
        return a+b;
    }
    public static double multiply(double a,double b)
    {
        return a*b;
    }
}
